package ph.com.medilink.maxiflexmobileapp.PlainOldJavaObjects;

import java.io.Serializable;

/**
 * Created by kurt_capatan on 6/29/2016.
 */
public class MedicalHistoryClass implements Serializable{

    private String Type;
    private String Month;
    private String Year;
    private String Reason;
    private String Remarks;

    public MedicalHistoryClass() {
    }

    public MedicalHistoryClass(String type, String month, String year, String reason, String remarks) {
        Type = type;
        Month = month;
        Year = year;
        Reason = reason;
        Remarks = remarks;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }

    public String getMonth() {
        return Month;
    }

    public void setMonth(String month) {
        Month = month;
    }

    public String getYear() {
        return Year;
    }

    public void setYear(String year) {
        Year = year;
    }

    public String getReason() {
        return Reason;
    }

    public void setReason(String reason) {
        Reason = reason;
    }

    public String getRemarks() {
        return Remarks;
    }

    public void setRemarks(String remarks) {
        Remarks = remarks;
    }
}
